fun main() {

    //1. Arithmetiic Operations-For simple calculations

    var x = 100
    var y = 10

    println(x+y)    //sum
    println(x-y)    //difference
    println(x*y)    //product
    println(x/y)    //quotient
    println(x%y)  //modulus


    //2. Comparison Operators- Return a boolean(True/False)
    var a = 89
    var b = 56

    println(a > b )
    println(a < b )
    println(a >= b )
    println(a <= b)
    println(a == b )
    println(a != b )

    //3. Assignment Operators- Assign values to variables
    var z = 45

    println(z)

    z+=5

    println(z)

    z /= 5

    println(z)


    //4. Logic operators - &&(and), or(||), not(!)

    var number = 78

    // && Returns True if both statements are true
    println(number >34 && number < 60)

    // || Returns True if one of the statements is true
    println(number >34 || number < 60)

    // ! returns False if the result is true
    println(!(number >34  || number < 60))









}